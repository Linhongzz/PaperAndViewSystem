package cn.edu.hebeu.pojo;

public class Tuzhi {
    private Long id;
    private Tuzhileibie leibie;
    private String tuming;
    private String tuhao;
    private String path;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Tuzhileibie getLeibie() {
        return leibie;
    }

    public void setLeibie(Tuzhileibie leibie) {
        this.leibie = leibie;
    }

    public String getTuming() {
        return tuming;
    }

    public void setTuming(String tuming) {
        this.tuming = tuming;
    }

    public String getTuhao() {
        return tuhao;
    }

    public void setTuhao(String tuhao) {
        this.tuhao = tuhao;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
